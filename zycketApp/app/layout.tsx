"use client";

import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";

import { EthersExtension } from "@dynamic-labs/ethers-v6";

import {
  DynamicContextProvider,
  DynamicWidget,
} from "@dynamic-labs/sdk-react-core";
import { DynamicWagmiConnector } from "@dynamic-labs/wagmi-connector";

import { EthereumWalletConnectors } from "@dynamic-labs/ethereum";
import { useEffect, useState } from "react";
import { createSmartAccount } from "@/components/biconomy/biconomy_chiliz";

const inter = Inter({ subsets: ["latin"] });

// export const metadata: Metadata = {
//   title: "Create Next App",
//   description: "Generated by create next app",
// };
import { evmNetworks } from "@/components/dynamic/evm-networks";
import Main from "@/components/biconomy/main";

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const [provider, setProvider] = useState<any>();
  const [signer, setSigner] = useState<any>();
  const [smartAccount, setSmartAccount] = useState<any>();

  useEffect(() => {
    const createAndSetSmartAccount = async () => {
      const newSmartAccount = await createSmartAccount(provider, signer);
      setSmartAccount(newSmartAccount);
      

    };

    if (provider && signer && !smartAccount) {
      console.log("Create smart account")
      createAndSetSmartAccount();
      console.log("Smart account created")
    }

    if (smartAccount) {
      console.log("Smart account: " + smartAccount)
      console.log(smartAccount)
    }
  }, [provider, signer, smartAccount]);

  return (
    <html lang="en">
      <body className={inter.className}>
          <DynamicContextProvider
          settings={{
            environmentId: process.env.NEXT_PUBLIC_DYNAMIC_ENVIRONMENT_ID || "",
            walletConnectors: [EthereumWalletConnectors],
            walletConnectorExtensions: [EthersExtension],
            evmNetworks,
          }}
        >
          <DynamicWagmiConnector>
            <DynamicWidget />
            <Main
              provider={provider}
              setProvider={setProvider}
              signer={signer}
              setSigner={setSigner}
            />
            {children}
          </DynamicWagmiConnector>
        </DynamicContextProvider>
      </body>
    </html>
  );
}
